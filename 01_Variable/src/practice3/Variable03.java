package practice3;

public class Variable03 {
	// main 메소드 내에 상수로 최대 크기를 1024로 초기화
	public static void main(String[] args) {
		// 상수 선언 => 예약어(final)사용
		// 자료형 변수명 final;
		// 명명 규칙
		// -대문자로만
		// -단어 사이 '_'로 구분
		final int MAX_SIZE = 1024;
		// MAX_SIZE = 2048;
		System.out.println(MAX_SIZE);
	}

}
